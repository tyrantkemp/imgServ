package xz.com.bean;

public class TopAndDown {

	private int   top;
	private int down;
	private String ImgId;
	
	public TopAndDown(){
		this.top=0;
		this.down=0;
		this.ImgId="";
	}
	public int getTop() {
		return top;
	}
	public void setTop(int top) {
		this.top = top;
	}
	public int getDown() {
		return down;
	}
	public void setDown(int down) {
		this.down = down;
	}
	public String getImgId() {
		return ImgId;
	}
	public void setImgId(String imgId) {
		ImgId = imgId;
	}

	
	
}
